// Helper function to get test by category ID
export function getCategoryTest(categoryId: string): CategoryTest | undefined {
  switch (categoryId) {
    case "A":
      return categoryATest
    case "C":
      return categoryCTest
    case "D":
      return categoryDTest
    case "E":
      return categoryETest
    case "F":
      return categoryFTest
    case "G":
      return categoryGTest
    case "H":
      return categoryHTest
    default:
      return undefined
  }
}

// Calculate test score
export function calculateTestScore(answers: number[], test: CategoryTest): {
  score: number
  percentage: number
  passed: boolean
  correctCount: number
  incorrectCount: number
} {
  let correctCount = 0
  
  for (let i = 0; i < test.questions.length; i++) {
    if (answers[i] === test.questions[i].correctAnswer) {
      correctCount++
    }
  }
  
  const percentage = Math.round((correctCount / test.totalQuestions) * 100)
  
  return {
    score: correctCount * 2, // 2 points per question = 100 max
    percentage,
    passed: percentage >= test.passingScore,
    correctCount,
    incorrectCount: test.totalQuestions - correctCount,
  }
}
