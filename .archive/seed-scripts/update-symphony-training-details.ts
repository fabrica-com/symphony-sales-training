async function updatesymphonyTrainingDetails() {
  console.log("Starting symphony training details update...")

  for (const training of symphonyTrainingDetails) {
    const { error } = await supabase
      .from("trainings")
      .update({ detail: training.detail })
      .eq("id", training.id)

    if (error) {
      console.error(`Error updating training ${training.id}:`, error)
    } else {
      console.log(`Updated training ${training.id} successfully`)
    }
  }

  console.log("symphony training details update completed!")
}

updatesymphonyTrainingDetails()
